package com.felipesantacruz.camelrun.holesfield;

public class ColorHole
{
	private String color;
	private int steps;
	
	public ColorHole(String color, int steps)
	{
		this.color = color;
		this.steps = steps;
	}

	public String getColor()
	{
		return color;
	}

	public int getSteps()
	{
		return steps;
	}
}
